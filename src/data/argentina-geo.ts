// Helper: buscar provincia por ID
export function getProvinciaById(id: string): Provincia | undefined {
  return PROVINCIAS_ARGENTINA.find((p) => p.id === id);
}

// Helper: buscar localidades de una provincia
export function getLocalidadesByProvinciaId(provinciaId: string): Localidad[] {
  const p = getProvinciaById(provinciaId);
  return p ? p.localidades : [];
}

// Helper: lista plana de todas las provincias (para dropdowns)
export function getProvinciasForSelect() {
  return PROVINCIAS_ARGENTINA.map((p) => ({
    value: p.id,
    label: p.nombre,
  }));
}
